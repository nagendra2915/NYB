// Grade calculation using if-else ladder
let marks = 96;

if (marks >= 90) 
    {
        console.log("Excellent!");
    }  
    else if (marks >= 80)  
    {
        console.log("Great Job!");
    }      
    else if (marks >= 70) 
    {
        console.log("Needs Improvement.");
    } 
    else if (marks >= 60) 
    {
        console.log("Passed, but Could Do Better.");
    } 
    else {
             console.log("Failed");
           }
